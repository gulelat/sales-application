package com.android.salesapp.bean;

public class QuetionBean 
{
	public int QuetionID;
	public String Quetion;
	public int AnsID;
	
	
	public int getQuetionID() {
		return QuetionID;
	}
	public void setQuetionID(int quetionID) {
		QuetionID = quetionID;
	}
	public String getQuetion() {
		return Quetion;
	}
	public void setQuetion(String quetion) {
		Quetion = quetion;
	}
	public int getAnsID() {
		return AnsID;
	}
	public void setAnsID(int ansID) {
		AnsID = ansID;
	}
	
	
	
}
